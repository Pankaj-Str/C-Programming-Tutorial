#include<stdio.h>

int main(){

    // Data type char

    char name[] = "Joy";
    char ltr = 'A';
    int age = 12;
    float height = 4.5;

    // print all variables

    printf("Your name is : %s\n",name);
    printf("Your letter is : %c\n",ltr);
    printf("Your age is : %d\n",age);
    printf("Your height is : %f\n",height);

    return 0;
}